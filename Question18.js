// How do you convert an object into array?
// We can convery an object to array using the enteries method which return the enteries of object in the form of array.
let obj = {
    a: 23,
    b: 34
}
console.log("Converting object to array: ", Object.entries(obj))

// Output
// Converting object to array:  [ [ 'a', 23 ], [ 
//     'b', 34 ] ]